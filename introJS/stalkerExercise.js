/* JS Stalker Exercise
variables, strings, prompt and console.console.log
- ask for users first name
- ask for users last name
- ask for users age
- print out users full name in a sentence
- print out user age in a sentence
*/

var firstName = prompt("What is your first name?");
var lastName = prompt("What is your last name?");
var age = prompt("What is your age?");
var fullName = firstName + " " + lastName;

console.log("Hello, your full name is " + fullName);
console.log("Your age is " + age);
